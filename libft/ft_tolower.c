#include "libft.h"

/*
**	возвращает букву в нижнем регистве, если агрумент int c является буквой
**	верхнего региства (от «А» до «Z»); в противном случае возвращает себя
*/

int	ft_tolower(int c)
{
	if (ft_isupper(c))
		return (c + 32);
	else
		return (c);
}
