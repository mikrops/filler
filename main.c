/*
**	Заполняем карту (ДОДЕЛАТЬ!!!)
*/
void	aggregate_plateau(t_plateau *plt)
{
	char	*str;
	int		i;
	int		j;

	str = "abc";
	i = 0;
	j = 0;
	get_next_line(0, &str);// пропускаем строку с номерами столбцов
	while (j < plt->n)
	{
		get_next_line(0, &str);
		while (i < plt->x)
		{
			plt->plateau[j][i] = str[i + 4];
			i++;
		}
		i = 0;
		j++;
	}
}

